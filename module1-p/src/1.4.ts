{
  // Basic Data Type

  // string

  let firstName: string = "Mahabub";
  // number
  let roll: number = 123;

  // boolean

  let isAdmin: boolean = true;

  // undefined

  let x: undefined = undefined;
  // null

  let y: null = null;
  let d: number;
  d = 123;

  // non primetive data type

  // array

  let friends: string[] = ["Mahabub", "Alom"];
  let eligibleRollList: number[] = [1, 2, 3];
  eligibleRollList.push(8);

  // tuple --> array --> order --> type of values

  let cordinates: [number, number] = [12, 55];
  let ageName: [number, string, boolean] = [21, "MR. X", true];

  ageName[0] = 52;
  ageName[1] = "mahaub";
  ageName[2] = false;
}
